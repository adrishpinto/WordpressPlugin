<div class="wrap">
    <h1>ActiveLoc Translator: Guide & Support</h1>

    <h2>Machine Translation (MT)</h2>
    <ol>
        <li><strong>Select languages:</strong> Choose the source and target languages for translation.</li>
        <li><strong>Select files:</strong> Pick the posts or files you want to translate.</li>
        <li><strong>Click "Translate Now":</strong> This button appears below the posts table.</li>
    </ol>

    <h3>MT Options</h3>
    <p><strong>Overwrite if title & language match:</strong> Checking this will replace existing translations that match the post title and language.</p>
    <p><strong>Publish translated posts:</strong> Automatically publish the translated posts after translation. Otherwise, they will remain as drafts.</p>

    <h2>Machine Translation Post-Editing (MTPE)</h2>
    <ol>
        <li><strong>Select languages:</strong> Choose source and target languages.</li>
        <li><strong>Select files:</strong> Pick the translated files you want to send for MTPE.</li>
        <li><strong>Click "Upload to MTPE":</strong> Button is located below the posts table.</li>
    </ol>

    <h3>Download MTPE Results</h3>
    <ol>
        <li>Click on <strong>"MTPE Completed List"</strong> near the filters or in the submenu.</li>
        <li>Click <strong>"View Status"</strong> for a file:</li>
        <ul>
            <li>If completed, you can:</li>
            <ul>
                <li>Download as a preview folder and view locally.</li>
                <li>Import as draft (recommended) or import as published.</li>
            </ul>
        </ul>
        <li><strong>File List:</strong> Shows the total list of files, but does not indicate completed files.</li>
    </ol>

    <p><em>Tip:</em> Always check the MTPE completed status before importing to ensure accurate translations.</p>
</div>